public class Parents {
    private String name ;
    private String job ;
    private int year ;

    public Parents(String name, String job, int year) {
        this.name = name;
        this.job = job;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }

    public int getYear() {
        return year;
    }
}
